import argparse
import pytest


def compute(data):

    outputs = []
    output_check = set([2, 3, 4, 7])
    count = 0
    for data_i in data.splitlines():
        data_split = data_i.split(" | ")
        outputs.append(data_split[1])
    for output_i in outputs:
        output_split = output_i.split(" ")
        for output in output_split:
            if len(output) in output_check:
                count += 1
    return count


